#include <stdio.h>
#include <stdlib.h>

/*
    Aula 24
    Operadores aritméticos

    Soma -> +
    Subtração -> -
    Multiplicação -> *
    Divisão -> /
*/

int main() {
    printf("Soma: %d\n", 10 + 20);
    printf("Subtração: %d\n", 10 - 20);
    printf("Multiplicação: %d\n", 10 * 20);
    printf("Divisão: %f\n", 10.0 / 20);

    return 0;
}